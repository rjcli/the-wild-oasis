import { formatDistance, parseISO, differenceInDays } from 'date-fns';

// Keep this helper compatible with both Date objects and ISO strings.
export const subtractDates = (dateStr1, dateStr2) =>
  differenceInDays(parseISO(String(dateStr1)), parseISO(String(dateStr2)));

export const formatDistanceFromNow = (dateStr) =>
  formatDistance(parseISO(dateStr), new Date(), {
    addSuffix: true,
  })
    .replace('about ', '')
    .replace('in', 'In');

// Return a stable ISO date string by normalizing the time component.
export const getToday = async (options = {}) => {
  const today = new Date();

  // Set end-of-day when comparing against timestamps created during the day.
  if (options?.end)
    // Set to the last second of the day
    today.setUTCHours(23, 59, 59, 999);
  else today.setUTCHours(0, 0, 0, 0);
  return today.toISOString();
};

export const formatCurrency = (value) =>
  new Intl.NumberFormat('en', { style: 'currency', currency: 'USD' }).format(
    value,
  );
